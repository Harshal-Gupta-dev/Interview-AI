const pdfParse = require("pdf-parse")
const { generateInterviewReport, generateResumePdf } = require("../services/ai.service")
const interviewReportModel = require('../models/interviewreport.model')
const { application } = require("express")



/**
 * @description Generate Interview Report based on the JobDescription, selfDescription and resume
 */

async function interviewReportController(req, res) {
    const resumeFile = req.file
    const parsedData = await (new pdfParse.PDFParse(Uint8Array.from(req.file.buffer))).getText()
    const resumeContent = await parsedData.text

    // const resumeContent = (await pdfParse(req.file.buffer)).text;
    const { selfDescription, jobDescription } = req.body


    const interviewReportbyAI = await generateInterviewReport({
        resume: resumeContent,
        selfDescription,
        jobDescription
    })

    const interviewReport = await interviewReportModel.create({
        user: req.user.id,
        resume: resumeContent,
        selfDescription,
        jobDescription,
        ...interviewReportbyAI
    })

    res.status(201).json({
        message: "Report Generate successfully",
        interviewReport
    })
}

/**
 * @description  Get the report by the id of th generated Report to display
 */

async function getinterviewReportbyIdController(req, res) {
    const { interviewId } = req.params;

    const interviewReport = await interviewReportModel.findOne({ _id: interviewId, user: req.user.id })

    if (!interviewReport) {
        res.status(404).json({
            message: "Report not found"
        })
    }

    res.status(200).json({
        message: "Interview Report found Successfully",
        interviewReport
    })
}

/**
 * @description Get all the interview reports generated by the user 
 */

async function getAllInterviewReportController(req, res) {

    const interviewReport = await interviewReportModel
        .find({ user: req.user.id })
        .sort({ createdAt: -1 })
        .select("-resume -selfDescription -jobDescription -technicalQuestion -behavioralQuestion -skillGaps -preparationPlan -createdAt -updatedAt -__v");

    res.status(200).json({
        message: "Report fetched Successfully",
        interviewReport
    })

}

async function generateResumePdfController(req, res) {
try{
    const { interviewReportId } = req.params
    const interviewReport = await interviewReportModel.findById(interviewReportId)

    if (!interviewReport) {
        return res.status(404).json({
            message: "Report not found"
        })
    }

    const { resume, selfDescription, jobDescription } = interviewReport

    const pdfBuffer = await generateResumePdf({ resume, selfDescription, jobDescription })
    res.set({
        "Content-Type": "application/pdf",
        "Content-Disposition": `attachment; filename=resume_${interviewReportId}.pdf`
    });

    return res.send(pdfBuffer)
    }catch(error){
        console.error("❌ Backend Error:", error.message);
        return res.status(500).json({ 
            success: false, 
            message: "Failed to generate PDF", 
            error: error.message 
        });
    }
}

module.exports = {
    interviewReportController,
    getinterviewReportbyIdController,
    getAllInterviewReportController,
    generateResumePdfController
}